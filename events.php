          <h2 id="chamber">
            Chamber Night
          </h2>
          <br>
          <p>
            Musicians from the Juneau String Ensemble group (Ursa Major, Ursa Minor, and Aurora Strings) as well as side quartets, trios, and duos gather for a night of chamber music at the Northern Lights United Church. This event is funded in part by the Juneau Arts and Humanities Council and by the City and Borough of Juneau. In the past, guest soloists visiting Juneau have accompanied students in their performances.
          </p>
          <br>
          <hr>

          <br>

          <h2 id="school">
            Juneau School District
          </h2>
          <br>
          <p>
            JAMM affiliated concerts are held during school assemblies and recital nights. Mock violins are crafted out of cardboard and students spend 2 months learning about the anatomy of the instrument as well as how to hold it before graduating to real ones. During concerts, these beginners will sing popular violin pieces such as Twinkle Twinkle Little Star while more advanced players will accompany them on their string instruments. Elements of dancing and Alaska Native Culture are also incorporated in these concerts.
            <br><br>
            At the middle school level and high school level, students can form string quartets under the instruction of Mr. Xia and use school concerts as a venue of performance. During the annual Southeast Music Festival, students at Thunder Mountain High School and Juneau-Douglas High School are encouraged to use Mr. Xia’s resources before adjudications and special performances.
          </p>
          <br>
          <hr>

