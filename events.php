            Recitals
          </h2>
          <br>
          <p>
            The culmination of the student’s hard work is rewarded at Mr. Xia’s annual spring and winter recital. These concerts are hosted at the Northern Lights United Church to showcase what students have been working towards yearlong.  Participants will work with piano accompaniment prior to the concert for solo works. Other musicians under the tutelage of Mr. Xia’s colleagues are invited to perform as well as various ensemble groups. The recital ends with a Suzuki run-through in which select Suzuki pieces are performed as a group and students join on stage as songs progress from medium to the beginner level. Refreshments and an order of congratulations round out the recital. This is a free event for students and parents but donations are welcomed to cover rental space.
          </p>
